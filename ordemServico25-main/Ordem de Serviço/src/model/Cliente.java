package model;

/**
 *
 * @author GERAL
 */
public class Cliente {
    String endereço;
    String nome;
    String email;
    String fone;

    public String getEndereço() {
        return endereço;
    }

    public void setEndereço(String endereço) {
        this.endereço = endereço;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getFone() {
        return fone;
    }

    public void setFone(String fone) {
        this.fone = fone;
    }

    public Cliente(String endereço, String nome, String email, String fone) {
        this.endereço = endereço;
        this.nome = nome;
        this.email = email;
        this.fone = fone;
    }
}
